          <div class="right-content">
            <span>Q1) ทำไมเวย์โปรตีนจึงสำคัญสำหรับคนออกกำลังกาย ?</span>
            <h5>A1 : ผู้เชี่ยวชาญได้อธิบายไว้ว่า “โปรตีน” คือองค์ประกอบสำคัญของกล้ามเนื้อ ซึ่งมีส่วนต่อการทำงานของระบบกล้ามเนื้อ หากได้รับโปรตีนอย่างเพียงพอกล้ามเนื้อจะยืดและหดตัวได้ดี จึงสามารถทำให้เราเคลื่อนไหวได้ดีด้วย รวมไปถึงทำให้การออกกำลังกายมีประสิทธิภาพมากยิ่งขึ้นด้วย</h5>
            <br>  
            <p>นอกจากนั้น ทุกครั้งหลังจากออกกำลังกายอย่างหนัก เซลล์กล้ามเนื้อบางส่วนอาจถูกใช้งานอย่างหนักจนเกิดการอักเสบ และมีความเสื่อมหรือทรุดโทรมไป อีกทั้งร่างกายยังเกิดอาการอ่อนล้า ทำให้ร่างกายต้องการโปรตีนเป็นอย่างมากเพื่อนำไปซ่อมแซม ฟื้นฟูกล้ามเนื้อที่เสื่อมหรือทรุดโทรมไปในขณะที่เราใช้ร่างกายอย่างหนักหน่วงและยังลดอาการอ่อนล้าหลังออกกำลังกายด้วย</p>

            <br>

            <span>Q2) ต้องทานเวย์โปรตีนแบบไหน ถึงจะเหมาะกับตนเอง ?</span>
            <h5> A2 : หลักๆ แล้วเวย์โปรตีนที่นิยมในท้องตลาดมีอยู่ 2 ชนิด คือ

เวย์โปรตีนคอนเซนเทรต (Whey Protein Concentrate – WPC) “สำหรับผู้ที่ต้องการพัฒนากล้ามเนื้อในช่วงแรกของการออกกำลังกายหรือ เริ่มต้นออกกำลังกาย ” เวย์โปรตีนชนิดนี้ตอบโจทย์ได้ดี  เพราะเป็นพื้นฐานเริ่มต้นในการเติมเต็มกล้ามเนื้อให้สมบูรณ์ขึ้น และช่วยเสริมสร้าง ฟื้นฟู ซ่อมแซมกล้ามเนื้อได้ดีและมีการสกัดน้ำตาลแลคโตสและไขมันที่ผสมอยู่ในนมออกบางส่วน  ทำให้ได้โปรตีนที่มีความเข้มข้นหลายระดับ ในช่วง 25-89 เปอร์เซ็นต์ และยังมีรสชาติที่อร่อยอยู่เนื่องจากมีแลคโตสและไขมันจำนวนหนึ่งอยู่นั่นเอง </h5>
            <br>  
            <p> เวย์โปรตีนไอโซเลท (Whey Protein Isolate – WPI) เป็นที่นิยมเป็นอย่างมากในคนที่ “ต้องการออกกำลังกายเพื่อกล้ามเนื้อที่เฟิร์มกระชับ และ เหมาะกับคนที่เสริมโปรตีนแบบไม่อ้วน”  โปรตีนชนิดนี้มีไขมันต่ำ เพราะได้ผ่านกรรมวิธีเพื่อสกัดให้ได้โปรตีนเข้มข้นถึง 90-97 เปอร์เซ็นต์ จึงมีไขมันและน้ำตาลแลคโตสที่น้อย  อีกทั้งมีอนุภาคของโปรตีนที่เล็กจึงดูดซึมเข้าสู่ร่างกายได้รวดเร็ว นำไปใช้ได้เร็ว ทำให้มีแรงในการออกกำลังกาย และโปรตีนยังมีส่วนช่วยในขบวนการเร่งการเผาผลาญไขมัน ส่งผลให้กล้ามเนื้อลีนกระชับอย่างเห็นผล  เป็นที่ชื่นชอบของคุณผู้หญิงที่รักการออกกำลังกายเป็นอย่างยิ่ง</p>

            <br>

            <span>Q3) ทานเวย์โปรตีนแล้วอ้วนจริงไหม ?</span>
            <h5>A3 : เป็นคำถามที่พบบ่อยไม่แพ้คำถามอื่น ซึ่งความเชื่อนี้ไม่เป็นความจริง  ว่าด้วยเรื่องความอ้วน การที่จะอ้วนได้ตัวแปรสำคัญคือ “แคลอรี่” ในแต่ละวันเรามีการทานอาหารธรรมชาติซึ่งมีแคลลอรี่หรือพลังงานที่ได้รับอยู่แล้ว การที่ทานเวย์โปรตีนเพิ่มเข้าไปโดยไม่ได้นับแคลอรี่ก่อนก็จะทำให้มีแคลลอรี่ในแต่ละวันที่มากเกินไป เป็นสาเหตุที่ทำให้อ้วนได้</h5>
            <br>  
            <p>    ในทางกลับกันหากหันมานับแคลลอรี่ทั้งการทานอาหารแบบปกติและการทานเวย์โปรตีนก็จะทำให้รู้ค่าแคลลอรี่ในแต่ละวันที่ร่างกายควรได้รับได้</p>

            <br>

            <span>Q4) แพ้นมวัว สามารถทานได้ไหม ?</span>
            <h5> A4 : ส่วนใหญ่แล้วผู้ที่แพ้นมวัว มักแพ้แลคโตสที่อยู่ในนม เมื่อทานเข้าไปแล้วจะทำให้เกิดอาการท้องอืดและท้องเสียในเวลาต่อมา ซึ่งเกิดจากร่างกายที่ไม่สามารถผลิตเอนไซม์ที่ย่อยแลคโตสได้ ทำให้เกิดก๊าซในท้องขึ้น ทั้งนี้ผู้ที่มีอาการแพ้นมวัวใช่ว่าจะไม่สามารถทานเวย์โปรตีนได้เลย เพียงแค่ต้องเลือกเวย์โปรตีนที่มีปริมาณของแลคโตสที่ต่ำ อย่างเช่น “เวย์โปรตีนไอโซเลท” เป็นต้น</h5>
